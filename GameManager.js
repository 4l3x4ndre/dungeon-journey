GameManager.prototype.levelHandler = function() {
    if (this.enemiesRemaining <= 0) {
        this.changeLevel();
    }

}

GameManager.prototype.firstLevel = function() {
    this.lvlId = -1;
    this.changeLevel();
}

GameManager.prototype.changeLevel = function () {
    if (this.lvlId + 1 < this.levels.length) {
        console.log("CHANGE");
        if (map != null) {
            draw(map.tile_sheet.door_open_image_index, map.canvas.getContext("2d"), map.door_open_pos[0], map.door_open_pos[1]);
        }
        if (!this.door_open) {
            this.door_open = true;
        }
        if (player != null && player.dst_open_door <= this.player_door_dst) {
            
            // New stats            
            this.door_open = false;
            this.lvlId ++;
            this.current_level = this.levels[this.lvlId];
            this.current_level = this.levels[this.lvlId];
            this.enemiesRemaining = enemiesHandler.enemy_density[this.lvlId];

            if (enemiesHandler != null) {
                this.enemiesRemaining = enemiesHandler.enemy_density[this.lvlId];
                enemiesHandler.setEnemies();
            }
            if (propsHandler != null) {
                propsHandler.clearProps();
                //propsHandler.setProps();
            }
            if (player != null) {
                player.x = map.door_closed_pos[0];
                player.y = map.door_closed_pos[1];
            }
            for (var i in bullets) {
                var b = bullets[i];
                delete bullets[b.id];
            }

            //this.propsDrawer();
        }
    } else {
        if (map != null) {
            draw(map.tile_sheet.door_open_image_index, map.canvas.getContext("2d"), map.door_open_pos[0], map.door_open_pos[1]);
        }
        if (!this.door_open) {
            this.door_open = true;
        }
        if (player != null && player.dst_open_door <= this.player_door_dst) {
            propsHandler.clearProps();
            this.end = true;
            player.x = front_canvas.width/2;
            player.y = front_canvas.height/2;
        }
    }
}

GameManager.prototype.propsDrawer = function() {
    for (var i in this.current_level) {
        const e = this.current_level[i];
        if (e >= props_tile_index[0] && e <= props_tile_index[1] ) {
            var dx = (i%map.columns) * map.tile_size;
            var dy = Math.floor(i/map.columns) * map.tile_size;
            var tile_index = this.current_level[i];
            props[props_index] = new Prop(dx, dy, tile_index);
            if (map != null) {
                draw(tile_index, document.getElementById("backcanvas").getContext("2d"), dx, dy);
            }
            props_index ++;
        }
    }
}